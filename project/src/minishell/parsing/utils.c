#include "../includes/minishell.h"

int	is_space(int c)
{
	return (c == ' ' || c == '\t' || c == '\n'
		||c == '\r' || c == '\v' || c == '\f');
}

int	is_op1(char c)
{
	return (c == '|' || c == '<' || c == '>');
}

